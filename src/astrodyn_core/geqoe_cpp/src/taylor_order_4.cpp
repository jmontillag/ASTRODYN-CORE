constexpr std::size_t STATE_DIM = 6;

inline std::size_t idx_yprop(std::size_t step, std::size_t comp) {
    return step * STATE_DIM + comp;
}

inline double derivatives_of_inverse_4(double a0, double a1, double a2, double a3) {
    double out = 0.0;
    const double in_a[4] = {a0, a1, a2, a3};
    astrodyn_core::math::compute_derivatives_of_inverse(in_a, &out, 4, true);
    return out;
}

inline double derivatives_of_inverse_wrt_param_4(double a0, double a1, double a2, double a3, double ad0, double ad1, double ad2, double ad3) {
    double out = 0.0;
    const double in_a[4] = {a0, a1, a2, a3};
    const double in_d[4] = {ad0, ad1, ad2, ad3};
    astrodyn_core::math::compute_derivatives_of_inverse_wrt_param(in_a, in_d, &out, 4, true);
    return out;
}

inline double derivatives_of_product_4(double a0, double a1, double a2, double a3) {
    double out = 0.0;
    const double in_a[4] = {a0, a1, a2, a3};
    astrodyn_core::math::compute_derivatives_of_product(in_a, &out, 4, true);
    return out;
}

inline double derivatives_of_product_wrt_param_4(double a0, double a1, double a2, double a3, double ad0, double ad1, double ad2, double ad3) {
    double out = 0.0;
    const double in_a[4] = {a0, a1, a2, a3};
    const double in_d[4] = {ad0, ad1, ad2, ad3};
    astrodyn_core::math::compute_derivatives_of_product_wrt_param(in_a, in_d, &out, 4, true);
    return out;
}

